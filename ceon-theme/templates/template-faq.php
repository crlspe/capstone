                </div>
                <div class="collapsible-body">
                    <span>
                        Once a candidate submits his/her application, the timeline is approximately 30 days to vet and
                        interview. The membership process is as follows:
                        <p>
                            <br />- The candidate is invited to attend up to two (2) monthly meetings to get a feel for
                            our organization before applying for membership;
                            <br />- The CEO Netweavers member who invited the potential candidate and a member of the
                            membership Committee discuss the candidate’s membership criteria and interest in our
                            organization;
                            <br />- If the CEON member (sponsor) recommends the candidate for membership, the sponsor or
                            a member of the Membership Committee provides the candidate the membership application link
                            to apply;
                            <br />- The candidate completes the application, which is then reviewed by the Membership
                            Committee against our stated criteria. If the candidate meets the criteria, then two
                            interviews are set up with the candidate: one with a member of the Membership Committee and
                            one with a member of the Operating Committee;
                            <br />- The Executive Director and the membership Committee Chair review the interview
                            results and follow-up with the candidate or sponsor if further clarification is needed;
                            <br />- The interview results are reviewed with the entire membership committee and a
                            decision is made on whether or not to recommend the candidate for membership;
                            <br />- If the Membership Committee supports extending an invitation to join, the Chair of
                            the Membership Committee recommends the candidate for membership at the next Operating
                            Committee meeting;
                            <br />- If the candidate does not meet the criteria outlined for membership, the Membership
                            Committee Chair will discuss with the CEO member (sponsor) and the decision is then
                            communicated to the candidate.
